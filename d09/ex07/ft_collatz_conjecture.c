unsigned int	ft_collatz_conjecture(unsigned int base)
{
	if (base == 1)
		return (0);
	if (base % 2 == 0)
		base = base / 2;
	else if (base % 2 == 1)
		base = base * 3 + 1;
	return (1 + ft_collatz_conjecture(base));
}
